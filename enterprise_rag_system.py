class DocumentChunk:
    """A single chunk of text with source metadata and RBAC tags."""
    def __init__(self, text, source, chunk_type, allowed_roles, extra_meta=None):
        self.text = text
        self.source = source
        self.chunk_type = chunk_type
        self.allowed_roles = allowed_roles
        self.extra_meta = extra_meta or {}
        # Generate unique chunk ID for traceability
        self.chunk_id = hashlib.md5(f"{source}:{text[:50]}".encode()).hexdigest()[:12]

    def to_metadata(self):
        meta = {
            "source": self.source,
            "chunk_type": self.chunk_type,
            "allowed_roles_str": ",".join(sorted(self.allowed_roles)),
            "chunk_id": self.chunk_id,
            "author": "Abhay Gupta",
        }
        # Store each role as an individual flag for ChromaDB filtering
        all_possible_roles = ["HR", "Admin", "Finance", "General_Employee"]
        for role in all_possible_roles:
            meta[f"role_{role}"] = "yes" if role in self.allowed_roles else "no"
        meta.update(self.extra_meta)
        return meta

# %%
def ingest_pdf(pdf_path: Path) -> list:
    """Extract text from PDF using PyMuPDF, chunk by sections."""
    doc = fitz.open(str(pdf_path))
    chunks = []
    for page_num in range(len(doc)):
        page = doc[page_num]
        text = page.get_text()
        sections = re.split(r'(SECTION \d+:.*)', text)
        for i, section in enumerate(sections):
            section = section.strip()
            if len(section) < 20:
                continue
            chunk = DocumentChunk(
                text=section,
                source=f"HR_Policy.pdf (Page {page_num + 1})",
                chunk_type="policy_document",
                allowed_roles=["HR", "Admin"],
                extra_meta={"page": str(page_num + 1)}
            )
            chunks.append(chunk)
    doc.close()
    print(f"[PDF] Ingested {len(chunks)} chunks from {pdf_path.name}")
    return chunks

# %%
def ingest_csv(csv_path: Path) -> list:
    """Convert CSV rows into tagged text chunks."""
    df = pd.read_csv(csv_path)
    chunks = []
    for _, row in df.iterrows():
        text = (
            f"{row['Quarter']} {row['Year']} | {row['Department']} ({row['Region']}): "
            f"Revenue=${row['Revenue']:,.0f}, Expenses=${row['Expenses']:,.0f}, "
            f"Net_Profit=${row['Net_Profit']:,.0f}, Growth={row['Growth_Rate']}%"
        )
        chunk = DocumentChunk(
            text=text,
            source=f"Financial_Records.csv",
            chunk_type="financial_data",
            allowed_roles=["Finance", "Admin"],
            extra_meta={
                "quarter": row["Quarter"],
                "year": str(row["Year"]),
                "department": row["Department"]
            }
        )
        chunks.append(chunk)
    # Add summary chunk accessible to General_Employee
    summary = (
        f"Financial Summary: Total Revenue across all periods = "
        f"${df['Revenue'].sum():,.0f}. Total Expenses = ${df['Expenses'].sum():,.0f}. "
        f"Overall Net Profit = ${df['Net_Profit'].sum():,.0f}."
    )
    chunks.append(DocumentChunk(
        text=summary,
        source="Financial_Records.csv (Summary)",
        chunk_type="financial_summary",
        allowed_roles=["Finance", "Admin", "General_Employee"],
    ))
    print(f"[CSV] Ingested {len(chunks)} chunks from {csv_path.name}")
    return chunks

