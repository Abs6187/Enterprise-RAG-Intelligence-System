# ## Step 8: Confidence Scoring

# %%
def compute_confidence(retrieved: dict) -> dict:
    """Compute retrieval confidence metrics."""
    distances = retrieved["distances"]
    similarities = [1 - d for d in distances]

    avg_sim = sum(similarities) / len(similarities) if similarities else 0
    max_sim = max(similarities) if similarities else 0
    min_sim = min(similarities) if similarities else 0

    # Retrieval confidence tier
    if avg_sim >= 0.5:
        tier = "HIGH"
    elif avg_sim >= 0.3:
        tier = "MEDIUM"
    else:
        tier = "LOW"

    return {
        "avg_similarity": round(avg_sim, 4),
        "max_similarity": round(max_sim, 4),
        "min_similarity": round(min_sim, 4),
        "num_results": len(distances),
        "confidence_tier": tier,
    }

