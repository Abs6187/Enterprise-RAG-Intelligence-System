# ## Step 6: RBAC-Filtered Retrieval

# %%
def rbac_retrieve(query: str, username: str, top_k: int = 5) -> dict:
    """
    Retrieve documents filtered by the user's RBAC roles.
    This is PRE-FILTERING: unauthorized data never enters the context.
    """
    user_roles = get_user_roles(username)

    # Build OR filter using role flag fields (role_HR, role_Finance, etc.)
    role_filters = []
    for role in user_roles:
        role_filters.append({f"role_{role}": {"$eq": "yes"}})

    if len(role_filters) == 1:
        where_filter = role_filters[0]
    else:
        where_filter = {"$or": role_filters}

    results = collection.query(
        query_texts=[query],
        n_results=top_k,
        where=where_filter,
        include=["documents", "metadatas", "distances"]
    )

    return {
        "query": query,
        "username": username,
        "roles": user_roles,
        "documents": results["documents"][0],
        "metadatas": results["metadatas"][0],
        "distances": results["distances"][0],
    }

