# ## Step 9: LLM Generation via DeepSeek API (with Offline Fallback)
# Uses DeepSeek V4 Flash via OpenAI-compatible endpoint. No `openai` package needed — raw HTTP calls.

# %%
def call_deepseek_api(prompt: str) -> str:
    """Call DeepSeek API using raw urllib (no openai package dependency)."""
    payload = json.dumps({
        "model": DEEPSEEK_MODEL,
        "messages": [{"role": "user", "content": prompt}],
        "temperature": 0.1,
        "max_tokens": 800,
        "stream": False,
    }).encode("utf-8")

    req = urllib.request.Request(
        DEEPSEEK_BASE_URL,
        data=payload,
        headers={
            "Content-Type": "application/json",
            "Authorization": f"Bearer {API_KEY}",
        },
        method="POST",
    )

    try:
        with urllib.request.urlopen(req, timeout=30) as resp:
            data = json.loads(resp.read().decode("utf-8"))
            return data["choices"][0]["message"]["content"]
    except urllib.error.HTTPError as e:
        body = e.read().decode("utf-8", errors="replace")
        print(f"[DeepSeek] HTTP {e.code}: {body[:200]}")
        raise
    except Exception as e:
        print(f"[DeepSeek] Request failed: {e}")
        raise


def generate_answer_offline(prompt: str, retrieved: dict) -> str:
    """Rule-based fallback when no API key is available."""
    docs = retrieved["documents"]
    metas = retrieved["metadatas"]
    query_lower = retrieved["query"].lower()

    answer_parts = ["Based on the retrieved documents:\n"]
    for i, (doc, meta) in enumerate(zip(docs, metas), 1):
        doc_lower = doc.lower()
        query_words = set(query_lower.split())
        overlap = sum(1 for w in query_words if w in doc_lower)
        if overlap >= 1:
            sentences = [s.strip() for s in doc.split(".") if len(s.strip()) > 20]
            if sentences:
                best = max(sentences, key=lambda s: sum(1 for w in query_words if w in s.lower()))
                answer_parts.append(f"- {best.strip('.')}. [{i}]")

    if len(answer_parts) == 1:
        answer_parts.append("Information not accessible or unavailable for your role.")

    answer_parts.append(f"\n\nSources: " + ", ".join(
        f"[{i+1}] {m['source']}" for i, m in enumerate(metas)
    ))
    return "\n".join(answer_parts)


def generate_answer(prompt: str, retrieved: dict) -> str:
    """Try DeepSeek API first, fall back to offline generation."""
    if API_KEY and not API_KEY.startswith("sk-your"):
        try:
            return call_deepseek_api(prompt)
        except Exception as e:
            print(f"[LLM] DeepSeek API failed: {e}. Using offline fallback.")

    print("[LLM] No API key configured. Using offline rule-based generation.")
    return generate_answer_offline(prompt, retrieved)

