def generate_hr_pdf(text_path: Path, pdf_path: Path):
    """Convert HR policy text file into a proper PDF document."""
    content = text_path.read_text(encoding="utf-8")
    pdf = FPDF()
    pdf.set_auto_page_break(auto=True, margin=15)
    pdf.add_page()

    pdf.set_font("Helvetica", "B", 16)
    pdf.cell(0, 10, "ACME CORPORATION", align="C")
    pdf.ln()
    pdf.set_font("Helvetica", "", 10)
    pdf.cell(0, 6, "Human Resources Policy Manual v3.2", align="C")
    pdf.ln(10)

    pdf.set_font("Helvetica", "", 9)
    for line in content.split("\n"):
        line = line.strip()
        if line.startswith("ACME CORPORATION") or line.startswith("Version 3.2"):
            continue
        if not line or all(ord(c) > 127 for c in line):
            pdf.ln(4)
            continue
        safe = ''.join(c if ord(c) < 128 else ' ' for c in line)
        if not safe.strip() or set(safe.strip()) <= {'-', '=', '_'}:
            pdf.ln(4)
            continue
        pdf.set_x(pdf.l_margin)
        if safe.startswith("SECTION"):
            pdf.set_font("Helvetica", "B", 12)
            pdf.cell(0, 7, safe)
            pdf.ln()
            pdf.set_font("Helvetica", "", 9)
        elif len(safe) > 2 and safe[0].isdigit() and "." in safe[:4]:
            pdf.set_font("Helvetica", "B", 10)
            pdf.cell(0, 6, safe)
            pdf.ln()
            pdf.set_font("Helvetica", "", 9)
        else:
            pdf.write(5, safe)
            pdf.ln()

    # Add author watermark on last page
    pdf.ln(10)
    pdf.set_font("Helvetica", "I", 7)
    pdf.write(4, "Generated by Enterprise RAG System | Author: Abhay Gupta | github.com/Abs6187")

    pdf.output(str(pdf_path))
    print(f"[OK] Generated PDF: {pdf_path} ({pdf_path.stat().st_size:,} bytes)")

generate_hr_pdf(HR_TEXT_FILE, HR_PDF_FILE)

