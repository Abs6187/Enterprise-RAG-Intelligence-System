def ingest_json_logs(json_path: Path) -> list:
    """Parse system logs and tag with their embedded RBAC roles."""
    with open(json_path, "r") as f:
        logs = json.load(f)
    chunks = []
    for log in logs:
        text = (
            f"[{log['timestamp']}] {log['severity']} | {log['service']} | "
            f"{log['error_code']}: {log['message']}"
        )
        chunk = DocumentChunk(
            text=text,
            source="System_Logs.json",
            chunk_type="system_log",
            allowed_roles=log["allowed_roles"],
            extra_meta={
                "severity": log["severity"],
                "service": log["service"],
                "error_code": log["error_code"]
            }
        )
        chunks.append(chunk)
    print(f"[JSON] Ingested {len(chunks)} chunks from {json_path.name}")
    return chunks

