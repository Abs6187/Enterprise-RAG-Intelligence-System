# ## Step 7: Citation-Aware Prompt Construction

# %%
def build_cited_prompt(query: str, retrieved: dict) -> str:
    """Build a prompt with numbered context citations."""
    context_blocks = []
    for i, (doc, meta, dist) in enumerate(zip(
        retrieved["documents"], retrieved["metadatas"], retrieved["distances"]
    ), 1):
        similarity = 1 - dist  # cosine distance to similarity
        context_blocks.append(
            f"Context ID [{i}] (Source: {meta['source']}, "
            f"Type: {meta['chunk_type']}, Similarity: {similarity:.3f}):\n{doc}"
        )

    context_text = "\n\n".join(context_blocks)

    prompt = f"""You are an Enterprise RAG assistant built by Abhay Gupta (github.com/Abs6187). Answer the user's question using ONLY the provided context below. Follow these rules strictly:

1. Use ONLY information from the provided context chunks.
2. Cite your sources using inline tags like [1], [2], etc.
3. If the answer cannot be found in the context, state: "Information not accessible or unavailable for your role."
4. List the key facts from the context before synthesizing your final answer.

--- RETRIEVED CONTEXT ---
{context_text}
--- END CONTEXT ---

User Role: {retrieved['roles']}
User Question: {query}

Answer (with citations):"""

    return prompt

