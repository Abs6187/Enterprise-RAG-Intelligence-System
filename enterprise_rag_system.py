# ## Step 10: Complete Query Pipeline

# %%
def query_with_rbac(username: str, question: str, top_k: int = 5, verbose: bool = True) -> dict:
    """Full RAG pipeline: authenticate -> retrieve -> generate -> cite.
    
    Built by Abhay Gupta (github.com/Abs6187)
    """

    print(f"\n{'='*70}")
    print(f"  USER: {username} | ROLES: {get_user_roles(username)}")
    print(f"  QUERY: {question}")
    print(f"{'='*70}")

    # 1. RBAC-filtered retrieval
    retrieved = rbac_retrieve(question, username, top_k)

    # 2. Confidence scoring
    confidence = compute_confidence(retrieved)

    # 3. Build cited prompt
    prompt = build_cited_prompt(question, retrieved)

    # 4. Generate answer
    answer = generate_answer(prompt, retrieved)

    # 5. Display results
    if verbose:
        print(f"\n  Confidence: {confidence['confidence_tier']} "
              f"(avg_sim={confidence['avg_similarity']:.3f})")
        print(f"  Retrieved {confidence['num_results']} chunks\n")
        print(f"  ANSWER:\n  {'-'*60}")
        for line in answer.split("\n"):
            print(f"  {line}")
        print(f"  {'-'*60}")
        print(f"\n  Retrieved Sources:")
        for i, meta in enumerate(retrieved["metadatas"], 1):
            sim = 1 - retrieved["distances"][i-1]
            print(f"    [{i}] {meta['source']} ({meta['chunk_type']}) "
                  f"sim={sim:.3f}")

    return {
        "username": username,
        "question": question,
        "answer": answer,
        "confidence": confidence,
        "sources": retrieved["metadatas"],
        "prompt": prompt,
    }

